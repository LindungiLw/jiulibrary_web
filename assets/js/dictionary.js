let currentLang = "en";

function changeLanguage(lang, event) {
  if (event) event.preventDefault();
  currentLang = lang;

  document.querySelectorAll(".current-lang-text").forEach((txt) => {
    txt.innerText = lang.toUpperCase();
  });

  document.querySelectorAll(".current-flag-img").forEach((img) => {
    if (lang === "en") {
      img.src = "https://flagcdn.com/w20/us.png";
    } else {
      img.src = "https://flagcdn.com/w20/id.png";
    }
  });

  document.querySelectorAll("[data-i18n]").forEach((elem) => {
    const key = elem.getAttribute("data-i18n");
    if (dict[lang] && dict[lang][key]) {
      if (elem.querySelector("span") && elem.querySelector("i")) {
        elem.querySelector("span").innerText = dict[lang][key];
      } else if (
        elem.querySelector("i") &&
        !elem.querySelector("span") &&
        !elem.querySelector("strong")
      ) {
        for (let i = 0; i < elem.childNodes.length; i++) {
          if (
            elem.childNodes[i].nodeType === 3 &&
            elem.childNodes[i].nodeValue.trim() !== ""
          ) {
            elem.childNodes[i].nodeValue = " " + dict[lang][key];
            break;
          }
        }
      } else if (elem.children.length > 0 && elem.querySelector("strong")) {
        elem.querySelector("strong").innerText = dict[lang][key];
      } else {
        elem.innerText = dict[lang][key];
      }
    }
  });

  const modColTitlesElem = document.getElementById("modColTitlesText");
  if (modColTitlesElem) modColTitlesElem.innerText = dict[lang].modColTitles;

  const modColCopiesElem = document.getElementById("modColCopiesText");
  if (modColCopiesElem) modColCopiesElem.innerText = dict[lang].modColCopies;

  document
    .querySelectorAll(".mod-hour-break")
    .forEach((el) => (el.innerText = dict[lang].modHourBreak));

  const searchInput = document.getElementById("navSearchInput");
  if (searchInput && dict[lang].navSearchPh) {
    searchInput.placeholder = dict[lang].navSearchPh;
  }

  const langMenu = document.getElementById("langMenu");
  if (langMenu) langMenu.classList.remove("show");
}
